#include <stdio.h>

/**
 * add - add two intgers
 * @a: first unt
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * mod - mod of two intgers
 * @a: first unt
 * @b: second int
 * Return: final
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - divide two intgers
 * @a: first unt
 * @b: second int
 * Return: qoutient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiply two intgers
 * @a: first unt
 * @b: second int
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub- subtract two intgers
 * @a: first unt
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
